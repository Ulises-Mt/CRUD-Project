    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (updateCliente != null) {
            updateCliente.actualizar(txtNoCliente1.getText(), txtNombre1.getText(),
                    txtTipoCliente1.getText(), txtRazonSocial1.getText());
            javax.swing.JOptionPane.showMessageDialog(this, "Cliente actualizado con éxito");         
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (updateCliente != null) {
            int respuesta = javax.swing.JOptionPane.showConfirmDialog(this,
                    "¿Deseas eliminar al cliente " + updateCliente.getNombre() + "?",
                    "Eliminación de Clientes",
                    javax.swing.JOptionPane.YES_NO_OPTION, javax.swing.JOptionPane.QUESTION_MESSAGE);

            if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
                updateCliente.eliminar();
                javax.swing.JOptionPane.showMessageDialog(this, "Cliente eliminado con éxito");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void lstClientesValueChanged(javax.swing.event.ListSelectionEvent evt) {
        if (!evt.getValueIsAdjusting()) {
            String registroSeleccionado = lstClientes.getSelectedValue();
            if (registroSeleccionado != null && !registroSeleccionado.equals("Historial de búsqueda...")) {
                String[] datos = registroSeleccionado.split("\\|");
                String noCliente = datos[0].replace("No. Cliente: ", "").trim();
                String nombre = datos[1].replace(" Nombre: ", "").trim();
                String tipo = datos[2].replace(" Tipo: ", "").trim();
                String razon = datos[3].replace(" Razon Social: ", "").trim();

                txtNoCliente1.setText(noCliente);
                txtNombre1.setText(nombre);
                txtTipoCliente1.setText(tipo);
                txtRazonSocial1.setText(razon);

                lblCliente.setText(noCliente);
                lblNombre.setText(nombre);
                lblTipo.setText(tipo);
                lblRazon.setText(razon);

                updateCliente = new clsClientes(Integer.parseInt(noCliente), nombre, tipo, razon);
            }
        }
    }

        private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {
                try {
                        clsClientes cCliente = new clsClientes(
                                        Integer.parseInt(txtNoCliente.getText()),
                                        txtNombre.getText(),
                                        txtTipoCliente.getText(),
                                        txtRazonSocial.getText());
                        cCliente.guardar();
                        txtNoCliente.setText("");
                        txtNombre.setText("");
                        txtTipoCliente.setText("");
                        txtRazonSocial.setText("");
                } catch (NumberFormatException e) {
                        javax.swing.JOptionPane.showMessageDialog(this,
                                        "El número de cliente debe ser un valor numérico.",
                                        "Error de entrada", javax.swing.JOptionPane.ERROR_MESSAGE);
                }
        }

        private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {
                clsClientes cCliente = new clsClientes();
                lstClientes.setModel(cCliente.llenarLista());
        }

